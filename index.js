// Looping In For
console.log("Looping For Pertama");
for (let i = 1; i <= 20; i++){
    console.log( i + " - I Love Coding");
}

console.log("looping For Kedua")
for (let i = 20; i >= 1; i--){
    console.log(i + " - I will Become Fullstack Web Developer");
}

// Looping In While
console.log("Looping While Pertama")
{
    let i = 2
    while (i <= 20){
        console.log(i + " - I Love Coding")
        i += 2
    }
}

console.log("Looping While Kedua")
{
    let i = 20
    while ( i >= 1) {
        console.log(i + " - I Will Become Fullstack Web Developer")
        i -= 2
    }
}